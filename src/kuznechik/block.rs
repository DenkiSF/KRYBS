use crate::kuznechik::operations::*;

#[derive(Debug, Clone)]
pub struct Block {
    data: [u8; BLOCK_SIZE]
}



impl Block {
    pub fn new(data: [u8;BLOCK_SIZE]) -> Self {
        Block { data }
    }

    pub fn get_block(&self) -> [u8; BLOCK_SIZE] {
        self.data
    }

    // Direct
    pub fn x(&mut self, round_key: [u8; BLOCK_SIZE]) {
        self.data = x_transform(self.data, round_key)
    }

    pub fn s(&mut self) {
        self.data = s_transform(self.data)
    }
    
    pub fn l(&mut self) {
        self.data = l_transform(&self.data)
    }

    // Inverse
    pub fn l_inv(&mut self) {
       self.data = l_inv_transform(&self.data)
    }

    pub fn s_inv(&mut self) {
        self.data = s_inv_transform(self.data)
    }
}










