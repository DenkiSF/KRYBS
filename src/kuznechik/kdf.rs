use crate::kuznechik::hmac::hmac_gostr3411_2012_256;
use crate::kuznechik::operations::KEY_SIZE;

pub fn kdf_gostr3411_2012_256(kin: &[u8], label: &[u8], seed: &[u8]) -> [u8; KEY_SIZE] {
    let data: Vec<u8> = [&[0x01], label, &[0x00], seed, &[0x01, 0x00]]
        .into_iter()
        .flat_map(|slice| slice.iter().copied())
        .collect();
    
    hmac_gostr3411_2012_256(kin, &data)
}

