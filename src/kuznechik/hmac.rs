use streebog::{Digest, Streebog256, Streebog512};

const BLOCK_SIZE: usize = 64;

pub fn hmac_gostr3411_2012_256(key: &[u8], data: &[u8]) -> [u8; 32] {
    hmac_internal::<Streebog256, 32>(key, data)
}

pub fn hmac_gostr3411_2012_512(key: &[u8], data: &[u8]) -> [u8; 64] {
    hmac_internal::<Streebog512, 64>(key, data)
}

fn hmac_internal<D, const N: usize>(key: &[u8], data: &[u8]) -> [u8; N]
where
    D: Digest,
{
    let k_star = prepare_key(key);
    
    let mut hasher = D::new();
    hasher.update(&xor_bytes(&k_star, 0x36));
    hasher.update(data);
    let inner_hash = hasher.finalize();
    
    let mut hasher = D::new();
    hasher.update(&xor_bytes(&k_star, 0x5C));
    hasher.update(&inner_hash);
    
    let result = hasher.finalize();
    let mut output = [0u8; N];
    output.copy_from_slice(&result[..N]);
    output
}

fn prepare_key(key: &[u8]) -> [u8; BLOCK_SIZE] {
    let mut k = [0u8; BLOCK_SIZE];
    let len = key.len().min(BLOCK_SIZE);
    k[..len].copy_from_slice(&key[..len]);
    k
}

fn xor_bytes(bytes: &[u8; BLOCK_SIZE], value: u8) -> [u8; BLOCK_SIZE] {
    bytes.map(|byte| byte ^ value)
}






